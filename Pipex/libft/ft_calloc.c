#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*p;
	size_t	overflow;

	if (!count || !size)
		return (ft_calloc(1, 1));
	overflow = count * size;
	if ((overflow / count != size))
		return (NULL);
	p = malloc(count * size);
	if (!p)
		return (NULL);
	ft_bzero(p, count * size);
	return (p);
}
